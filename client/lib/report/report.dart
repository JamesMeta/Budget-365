class Report {
  final String reportCode;
  final String amount;
  final String description;
  final String user;
  final String date;
  final String category;
  final int type;

  Report({
    required this.reportCode,
    required this.amount,
    required this.description,
    required this.user,
    required this.date,
    required this.category,
    required this.type,
  });
}
